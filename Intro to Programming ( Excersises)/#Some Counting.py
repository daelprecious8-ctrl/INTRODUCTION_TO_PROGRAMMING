#Some Counting 
# 1.
print(" 0 to 50:")
for i in range(0, 51, 1):
    print(i)
print()  # blank line for spacing

# 2. 
print("50 to 0:")
for i in range(50, -1, -1):
    print(i)
print()

# 3. 
print("30 to 50:")
for i in range(30, 51, 1):
    print(i)
print()

# 4. 
print("50 to 10 (counting by 2) ):")
for i in range(50, 9, -2):
    print(i)
print()

# 5. 
print("100 to 200 (counting by 5s):")
for i in range(100, 201, 5):
    print(i)
print()